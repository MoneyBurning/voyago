function getServiceClient() {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !serviceKey) {
    throw new Error(
      "NEXT_PUBLIC_SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY 환경변수가 필요합니다. " +
        "SUPABASE_SERVICE_ROLE_KEY는 RLS를 우회하므로 절대 클라이언트에 노출하지 마세요."
    );
  }
  return createClient(url, serviceKey);
}

/**
 * 다낭 city row를 upsert하고, 기존 places를 지운 뒤 시드 데이터를 새로 삽입한다.
 * (city_id 등 DB 생성 키에 unique 제약이 없으므로, 재실행해도 중복이 쌓이지 않도록
 * "삭제 후 재삽입" 방식으로 멱등성을 보장한다.)
 */
export async function seedDanang() {
  const supabase = getServiceClient();

  const { data: existingCity, error: findError } = await supabase
    .from("cities")
    .select("id")
    .eq("name_en", danangCity.name_en)
    .maybeSingle();
  if (findError) throw findError;

  let cityId: string;
  if (existingCity) {
    cityId = existingCity.id;
    const { error: updateError } = await supabase.from("cities").update(danangCity).eq("id", cityId);
    if (updateError) throw updateError;
  } else {
    const { data: inserted, error: insertCityError } = await supabase
      .from("cities")
      .insert(danangCity)
      .select("id")
      .single();
    if (insertCityError || !inserted) throw insertCityError ?? new Error("도시 생성 실패");
    cityId = inserted.id;
  }

  const { error: deleteError } = await supabase.from("places").delete().eq("city_id", cityId);
  if (deleteError) throw deleteError;

  const verifiedAt = new Date().toISOString();
  const allPlaces = [...restaurants, ...cafes, ...attractions, ...hotels].map((place) => ({
    ...place,
    city_id: cityId,
    verified_at: verifiedAt,
  }));

  const { error: insertPlacesError } = await supabase.from("places").insert(allPlaces);
  if (insertPlacesError) throw insertPlacesError;

  return { cityId, placesCount: allPlaces.length };
}

